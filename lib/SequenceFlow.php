<?php
class SequenceFlow
{
	protected $id;
	protected $name;
	protected $sourceRef;
	protected $targetRef;
	protected $type;

	function setId($id)
	{
		 $this->id = $id;
	}

	function getId()
	{
		return $this->id;
	}

	public function setName($name)
	{
		$this->name = $name;
	}

	public function getName()
	{
		return $this->name;
	}

	public function setSourceRef($source)
	{
		$this->sourceRef = $source;
	}

	public function getSourceRef()
	{
		return $this->sourceRef;
	}


	public function setTargetRef($target)
	{
		$this->targetRef = $target;
	}

	public function getTargetRef()
	{
		return $this->targetRef;
	}

	public function setType($type)
	{
		$this->type = $type;
	}

	public function getType()
	{
		return $this->type;
	}
}
?>
